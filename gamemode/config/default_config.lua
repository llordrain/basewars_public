function BaseWars:AddDefaultConfig(configKey, configType, config, configTranslation)
	if not configKey then
		return
	end

	if BaseWars.DefaultConfig[configKey] != nil then
		BaseWars:Warning("Config \"" .. configKey .. "\" already exists!")
		return
	end

	if not config then
		BaseWars:Warning("Missing value for \"" .. configKey .. "\"")
		return
	end

	if istable(config) and (not configType or configType == 0) then
		BaseWars:Warning("Wrong type for \"" .. configKey .. "\"")
		return
	end

	BaseWars.DefaultConfig[configKey] = config

	-- I dont need that if its not a table
	if configType and configType > 0 then
		BaseWars.DefaultConfigType[configKey] = configType
	end

	if configTranslation and istable(configTranslation) then
		for languageCode, translations in pairs(configTranslation) do
			languageCode = string.lower(languageCode)

			for k, v in pairs(translations) do
				if k == "name" then
					BaseWars.LANG[languageCode]["gamemodeConfig_" .. configKey .. "Name"] = v
				end

				if k == "desc" then
					BaseWars.LANG[languageCode]["gamemodeConfig_" .. configKey .. "Desc"] = v
				end

				if k == "extra" and istable(v) then
					BaseWars.LANG[languageCode]["gamemodeConfig_" .. configKey] = {}

					for extraKey, extraValue in pairs(v) do
						BaseWars.LANG[languageCode]["gamemodeConfig_" .. configKey][extraKey .. "Name"] = extraValue.name
						BaseWars.LANG[languageCode]["gamemodeConfig_" .. configKey][extraKey .. "Desc"] = extraValue.desc
					end
				end
			end
		end
	end
end

